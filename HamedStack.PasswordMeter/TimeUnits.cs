namespace HamedStack.PasswordMeter;

/// <summary>
/// Represents a structure for storing time units in a human-readable format.
/// </summary>
internal class TimeUnits
{
    /// <summary>
    /// Gets or sets the number of millennia.
    /// </summary>
    public int Millennium { get; init; }

    /// <summary>
    /// Gets or sets the number of centuries.
    /// </summary>
    public int Centuries { get; init; }

    /// <summary>
    /// Gets or sets the number of decades.
    /// </summary>
    public int Decades { get; init; }

    /// <summary>
    /// Gets or sets the number of years.
    /// </summary>
    public int Years { get; init; }

    /// <summary>
    /// Gets or sets the number of months.
    /// </summary>
    public int Months { get; init; }

    /// <summary>
    /// Gets or sets the number of days.
    /// </summary>
    public int Days { get; init; }

    /// <summary>
    /// Gets or sets the number of hours.
    /// </summary>
    public int Hours { get; init; }

    /// <summary>
    /// Gets or sets the number of minutes.
    /// </summary>
    public int Minutes { get; init; }

    /// <summary>
    /// Gets or sets the number of seconds.
    /// </summary>
    public int Seconds { get; init; }
}
